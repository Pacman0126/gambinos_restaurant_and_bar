def _clean_env_str(key: str, default: str = "") -> str:
    """
    Defensive: strips inline '# comments' and whitespace from .env values.
    This prevents weird unicode/comment artifacts from breaking SMTP auth.
    """
    raw = env(key, default=default)
    if raw is None:
        return ""
    raw = str(raw)
    if "#" in raw:
        raw = raw.split("#", 1)[0]
    return raw.strip()


